    private Usuario preencherUsuario() {
        try {
            objeto = new Usuario();
             
          //  objeto.setId(Integer.parseInt(txtID.getText()));
            objeto.setUsuario(txtUsuario.getText());
            objeto.setSenha(txtSenha1.getText());
            objeto.setEmail(txtEmail.getText());
            return objeto;
        } catch (Exception ex) {
            System.out.println("ERRO: " + ex.getMessage());
        }
        return null;

    }

    boolean validar() {
        try {
          
            if (txtUsuario.getText().equals("")) {
                Globais.exibirMensagem("Informe o Usuário");
                return false;
            } else if (txtSenha1.getText().equals("")) {
                Globais.exibirMensagem("Informe uma senha");
                return false;
            } else if (txtSenha2.getText().equals("")) {
                Globais.exibirMensagem("O campo de comfirmação de senha esta em branco!");
                return false;
            } else if (!txtSenha2.getText().equals(txtSenha1.getText())) {
                Globais.exibirMensagem("As duas senhas não são iguis!");
                return false;
            } else if (txtEmail.getText().equals("")) {
                Globais.exibirMensagem("Informe o E-mail!");
                return false;
            } else {
                return true;
            }

        } catch (Exception ex) {
            System.out.println("ERRO: " + ex.getMessage());
        }
        return true;

    }

