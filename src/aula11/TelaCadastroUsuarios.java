    private void bntSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bntSalvarActionPerformed
        try {
            boolean validou = validar();
           if(validou){
                
                preencherUsuario();
                
                if(objeto != null){
                    
                    controller = new UsuarioController();
                    boolean retorno = false;
                    
                    if(objeto.getId() == 0){
                        retorno = controller.incluir(objeto);
                    }else{
                        retorno = controller.alterar(objeto);
                    }
                    
                    if(retorno){
                        Globais.exibirMensagem("Sucesso!");
                    }else{
                        Globais.exibirMensagem("Erro ao salvar usuário!");
                    }
                }
            }

        } catch (Exception ex) {
            Globais.exibirMensagem("Erro ao salvar!");
        }


    }//GEN-LAST:event_bntSalvarActionPerformed

