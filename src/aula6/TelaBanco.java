    private void btnCadastroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastroActionPerformed
       ContaBanco objConta= new ContaBanco();
       
       objConta.setAgencia(txtAgencia.getText());
        objConta.setConta(txtConta.getText());
        objConta.setTitular(txtTitular.getText());
          objConta.setSaldo(Double.parseDouble(txtSaldo.getText()));
           objConta.setChequeEspecial(Double.parseDouble(txtChequeEspecial.getText()));
           objConta.setChavePIX(txtChavePix.getText());
           ///txtConta.setText(objConta.toString());
           
          /// cmbContas.addItem(objConta.getConta());
           
           
           model.addElement(objConta);
           
           cmbContas.setModel(model);
    }//GEN-LAST:event_btnCadastroActionPerformed
          
